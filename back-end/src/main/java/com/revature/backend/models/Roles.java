package com.revature.backend.models;

/**
 * This enum represents the different roles a user can have in the system.
 * Each instance of this enum corresponds to a specific role.
 */
public enum Roles {
    /**
     * Represents a student user.
     */
    STUDENT,

    /**
     * Represents a teacher user.
     */
    TEACHER,

    /**
     * Represents an admin user.
     */
    ADMIN
}